function getLessonIdFromUrl() {
  return new URLSearchParams(window.location.search).get("lesson");
}

function syncLessonQueryParam(sectionId) {
  if (!window.history || typeof window.history.replaceState !== "function") {
    return;
  }

  const url = new URL(window.location.href);
  url.searchParams.set("lesson", sectionId);
  window.history.replaceState({}, "", url.toString());
}

function escapeHTML(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;");
}

function renderNavigation() {
  navRoot.innerHTML = courseData.sections
    .map((section) => {
      const activeClass = section.id === state.activeSectionId ? "is-active" : "";
      const navTitle = section.navTitle || section.title;
      return `
        <button class="nav-button ${activeClass}" type="button" data-section="${section.id}">
          <span class="nav-number">${escapeHTML(section.navLabel || section.eyebrow || "")}</span>
          <span class="nav-title">${escapeHTML(navTitle)}</span>
        </button>
      `;
    })
    .join("");
}

function flashcardKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function quizKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function renderLessonBlocks(section) {
  const blocks = Array.isArray(section.blocks) ? section.blocks : [];
  if (blocks.length === 0) {
    return section.contentHtml || "";
  }

  return blocks
    .map((block) => {
      const paragraphs = Array.isArray(block.paragraphs)
        ? block.paragraphs.map((paragraph) => `<p>${escapeHTML(paragraph)}</p>`).join("")
        : "";
      const items = Array.isArray(block.items) && block.items.length > 0
        ? `<${block.ordered ? "ol" : "ul"}>${block.items.map((item) => `<li>${escapeHTML(item)}</li>`).join("")}</${block.ordered ? "ol" : "ul"}>`
        : "";
      return `
        <section class="callout">
          <span class="block-label">${escapeHTML(block.title)}</span>
          ${paragraphs}
          ${items}
        </section>
      `;
    })
    .join("");
}

function getSectionQuizList(section) {
  if (Array.isArray(section.quizzes)) {
    return section.quizzes;
  }

  if (section.reviewQuiz) {
    return [section.quiz, ...section.reviewQuiz].filter(Boolean);
  }

  return [section.quiz].filter(Boolean);
}

function getSectionQuestionCount(section) {
  return getSectionQuizList(section).length;
}

function getSectionQuiz(section, index) {
  return getSectionQuizList(section)[index];
}

function getSectionAnsweredCount(section) {
  const questionCount = getSectionQuestionCount(section);
  let answeredCount = 0;
  for (let index = 0; index < questionCount; index += 1) {
    if (state.answeredQuestions[quizKey(section.id, index)] !== undefined) {
      answeredCount += 1;
    }
  }
  return answeredCount;
}

