function flashcardKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function quizKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function getSectionQuizList(section) {
  if (Array.isArray(section.quizzes)) {
    return section.quizzes;
  }

  if (section.reviewQuiz) {
    return [section.quiz, ...section.reviewQuiz].filter(Boolean);
  }

  return [section.quiz].filter(Boolean);
}

function getSectionQuestionCount(section) {
  return getSectionQuizList(section).length;
}

function getSectionAnsweredCount(section) {
  const questionCount = getSectionQuestionCount(section);
  let answeredCount = 0;
  for (let index = 0; index < questionCount; index += 1) {
    if (state.answeredQuestions[quizKey(section.id, index)] !== undefined) {
      answeredCount += 1;
    }
  }
  return answeredCount;
}

