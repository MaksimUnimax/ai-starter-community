function updateProgress() {
  const section = courseData.sections.find((item) => item.id === state.activeSectionId) || courseData.sections[0];
  const totalQuestions = getSectionQuestionCount(section);
  const answeredQuestions = getSectionAnsweredCount(section);
  const progress = totalQuestions === 0 ? 0 : Math.min(100, Math.round((answeredQuestions / totalQuestions) * 100));

  progressFill.style.width = `${progress}%`;
  progressValue.textContent = `${progress}%`;

  if (progress === 0) {
    progressLabel.textContent = "Пока ничего не проверено.";
  } else if (answeredQuestions === 1) {
    progressLabel.textContent = `Выполнена 1 из ${totalQuestions} проверок.`;
  } else if (progress < 100) {
    progressLabel.textContent = `Выполнено ${answeredQuestions} из ${totalQuestions} проверок.`;
  } else {
    progressLabel.textContent = "Все проверки урока выполнены.";
  }
}

function renderActiveSection() {
  const section = courseData.sections.find((item) => item.id === state.activeSectionId) || courseData.sections[0];
  activeSectionRoot.innerHTML = renderSectionContent(section);
  renderNavigation();
  updateProgress();
}

function setActiveSection(sectionId, options = {}) {
  const normalizedSectionId = legacySectionAliases[sectionId] || sectionId;
  const resolvedSectionId = isValidSectionId(normalizedSectionId) ? normalizedSectionId : "lesson-1";
  const shouldUpdateUrl = options.updateUrl !== false;

  state.activeSectionId = resolvedSectionId;
  state.visitedSections.add(resolvedSectionId);
  if (shouldUpdateUrl) {
    syncLessonQueryParam(resolvedSectionId);
  }
  renderActiveSection();
}

document.addEventListener("click", (event) => {
  const starterPromptJump = event.target.closest("[data-starter-prompt-jump]");
  if (starterPromptJump) {
    event.preventDefault();
    const sectionId = starterPromptJump.dataset.starterPromptJump;
    const panel = document.querySelector(`[data-starter-prompt-panel="${sectionId}"]`);
    const textarea = panel?.querySelector(`textarea[id="${sectionId}-starter-prompt"]`);
    state.openStarterPromptIds.add(sectionId);
    if (textarea) {
      textarea.hidden = false;
    }
    const toggleButton = panel?.querySelector(`[data-starter-prompt-toggle="${sectionId}"]`);
    if (toggleButton) {
      toggleButton.textContent = "Скрыть prompt";
    }
    panel?.scrollIntoView({ behavior: "smooth", block: "start" });
    return;
  }

  const starterPromptToggle = event.target.closest("[data-starter-prompt-toggle]");
  if (starterPromptToggle) {
    const sectionId = starterPromptToggle.dataset.starterPromptToggle;
    const isOpen = state.openStarterPromptIds.has(sectionId);
    const panel = document.querySelector(`[data-starter-prompt-panel="${sectionId}"]`);
    const textarea = panel?.querySelector(`textarea[id="${sectionId}-starter-prompt"]`);
    if (isOpen) {
      state.openStarterPromptIds.delete(sectionId);
    } else {
      state.openStarterPromptIds.add(sectionId);
    }
    if (textarea) {
      textarea.hidden = isOpen;
    }
    starterPromptToggle.textContent = isOpen ? "Смотреть prompt" : "Скрыть prompt";
    return;
  }

  const navButton = event.target.closest("[data-section]");
  if (navButton) {
    setActiveSection(navButton.dataset.section);
    return;
  }

  const flashcard = event.target.closest("[data-flashcard]");
  if (flashcard) {
    state.flippedCards.has(flashcard.dataset.flashcard)
      ? state.flippedCards.delete(flashcard.dataset.flashcard)
      : state.flippedCards.add(flashcard.dataset.flashcard);
    renderActiveSection();
    return;
  }

  const quizButton = event.target.closest("[data-quiz]");
  if (quizButton) {
    const questionKey = quizButton.dataset.quiz;
    const [sectionId, quizIndexText] = questionKey.split(":");
    const quizIndex = Number(quizIndexText);
    const section = courseData.sections.find((item) => item.id === sectionId);
    const quiz = section ? getSectionQuiz(section, quizIndex) : null;
    if (!quiz) {
      return;
    }
    const selectedIndex = Number(quizButton.dataset.option);
    state.answeredQuestions[questionKey] = selectedIndex;
    if (selectedIndex === quiz.answerIndex) {
      state.solvedQuestions.add(questionKey);
    }
    renderActiveSection();
    return;
  }

  const starterPromptCopyButton = event.target.closest("[data-starter-prompt-copy]");
  if (starterPromptCopyButton) {
    const section = getStarterPromptSection(starterPromptCopyButton.dataset.starterPromptCopy);
    if (section?.starterPromptMarkdown) {
      copyTextToClipboard(section.starterPromptMarkdown);
    }
    return;
  }

  const starterPromptDownloadButton = event.target.closest("[data-starter-prompt-download]");
  if (starterPromptDownloadButton) {
    const section = getStarterPromptSection(starterPromptDownloadButton.dataset.starterPromptDownload);
    if (section?.starterPromptMarkdown) {
      const filename =
        starterPromptDownloadButton.dataset.starterPromptFilename ||
        section.starterPromptFilename ||
        "start_project_documentation_prompt.md";
      downloadMarkdown(filename, section.starterPromptMarkdown);
    }
    return;
  }

  const startButton = event.target.closest("#start-learning");
  if (startButton) {
    setActiveSection("lesson-1");
    return;
  }

  const reviewButton = event.target.closest("#open-review");
  if (reviewButton) {
    setActiveSection("review");
    return;
  }
});

