document.addEventListener("click", (event) => {
  const starterPromptJump = event.target.closest("[data-starter-prompt-jump]");
  if (starterPromptJump) {
    event.preventDefault();
    const sectionId = starterPromptJump.dataset.starterPromptJump;
    const panel = document.querySelector(`[data-starter-prompt-panel="${sectionId}"]`);
    const textarea = panel?.querySelector(`textarea[id="${sectionId}-starter-prompt"]`);
    state.openStarterPromptIds.add(sectionId);
    if (textarea) {
      textarea.hidden = false;
    }
    const toggleButton = panel?.querySelector(`[data-starter-prompt-toggle="${sectionId}"]`);
    if (toggleButton) {
      toggleButton.textContent = "Скрыть prompt";
    }
    panel?.scrollIntoView({ behavior: "smooth", block: "start" });
    return;
  }

  const starterPromptToggle = event.target.closest("[data-starter-prompt-toggle]");
  if (starterPromptToggle) {
    const sectionId = starterPromptToggle.dataset.starterPromptToggle;
    const isOpen = state.openStarterPromptIds.has(sectionId);
    const panel = document.querySelector(`[data-starter-prompt-panel="${sectionId}"]`);
    const textarea = panel?.querySelector(`textarea[id="${sectionId}-starter-prompt"]`);
    if (isOpen) {
      state.openStarterPromptIds.delete(sectionId);
    } else {
      state.openStarterPromptIds.add(sectionId);
    }
    if (textarea) {
      textarea.hidden = isOpen;
    }
    starterPromptToggle.textContent = isOpen ? "Смотреть prompt" : "Скрыть prompt";
    return;
  }

  const navButton = event.target.closest("[data-section-nav]");
  if (navButton) {
    setActiveSection(navButton.dataset.section, { scroll: false });
    return;
  }

  const practiceCarouselStep = event.target.closest("[data-practice-carousel-step]");
  if (practiceCarouselStep) {
    const carousel = practiceCarouselStep.closest("[data-practice-carousel]");
    if (!carousel) {
      return;
    }
    const activeIndex = Number(practiceCarouselStep.dataset.practiceCarouselStep);
    if (Number.isNaN(activeIndex)) {
      return;
    }
    setPracticeCarouselIndex(carousel, activeIndex);
    return;
  }

  const practiceCarouselPrev = event.target.closest("[data-practice-carousel-prev]");
  if (practiceCarouselPrev) {
    const carousel = practiceCarouselPrev.closest("[data-practice-carousel]");
    if (!carousel) {
      return;
    }
    const key = getPracticeCarouselKey(carousel);
    const currentIndex = state.practiceCarouselState[key] ?? 0;
    setPracticeCarouselIndex(carousel, Math.max(0, currentIndex - 1));
    return;
  }

  const practiceCarouselNext = event.target.closest("[data-practice-carousel-next]");
  if (practiceCarouselNext) {
    const carousel = practiceCarouselNext.closest("[data-practice-carousel]");
    if (!carousel) {
      return;
    }
    const slides = carousel.querySelectorAll("[data-practice-carousel-slide]");
    const key = getPracticeCarouselKey(carousel);
    const currentIndex = state.practiceCarouselState[key] ?? 0;
    setPracticeCarouselIndex(carousel, Math.min(slides.length - 1, currentIndex + 1));
    return;
  }

  const sectionButton = event.target.closest("[data-section]");
  if (sectionButton) {
    const shouldScroll = sectionButton.dataset.sectionFooterNav === "true";
    setActiveSection(sectionButton.dataset.section, { scroll: shouldScroll });
    return;
  }

  const flashcard = event.target.closest("[data-flashcard]");
  if (flashcard) {
    state.flippedCards.has(flashcard.dataset.flashcard)
      ? state.flippedCards.delete(flashcard.dataset.flashcard)
      : state.flippedCards.add(flashcard.dataset.flashcard);
    renderActiveSection();
    return;
  }

  const quizButton = event.target.closest("[data-quiz]");
  if (quizButton) {
    const questionKey = quizButton.dataset.quiz;
    const [sectionId, quizIndexText] = questionKey.split(":");
    const quizIndex = Number(quizIndexText);
    const section = courseData.sections.find((item) => item.id === sectionId);
    const quiz = section ? getSectionQuiz(section, quizIndex) : null;
    if (!quiz) {
      return;
    }
    const selectedIndex = Number(quizButton.dataset.option);
    state.answeredQuestions[questionKey] = selectedIndex;
    if (selectedIndex === quiz.answerIndex) {
      state.solvedQuestions.add(questionKey);
    }
    renderActiveSection();
    return;
  }

  const starterPromptCopyButton = event.target.closest("[data-starter-prompt-copy]");
  if (starterPromptCopyButton) {
    const promptId = starterPromptCopyButton.dataset.starterPromptCopy;
    const promptMarkdown = getStarterPromptMarkdown(promptId);
    if (promptMarkdown) {
      copyTextToClipboard(promptMarkdown);
    }
    return;
  }

  const starterPromptDownloadButton = event.target.closest("[data-starter-prompt-download]");
  if (starterPromptDownloadButton) {
    const promptId = starterPromptDownloadButton.dataset.starterPromptDownload;
    const promptMarkdown = getStarterPromptMarkdown(promptId);
    if (promptMarkdown) {
      const filename =
        starterPromptDownloadButton.dataset.starterPromptFilename ||
        "start_project_documentation_prompt.md";
      downloadMarkdown(filename, promptMarkdown);
    }
    return;
  }

  const startButton = event.target.closest("#start-learning");
  if (startButton) {
    setActiveSection("lesson-1");
    return;
  }

  const reviewButton = event.target.closest("#open-review");
  if (reviewButton) {
    setActiveSection("review");
    return;
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key !== "ArrowLeft" && event.key !== "ArrowRight") {
    return;
  }

  const carousel = event.target.closest?.("[data-practice-carousel]");
  if (!carousel) {
    return;
  }

  const slides = carousel.querySelectorAll("[data-practice-carousel-slide]");
  if (slides.length === 0) {
    return;
  }

  const key = getPracticeCarouselKey(carousel);
  const currentIndex = state.practiceCarouselState[key] ?? 0;
  const nextIndex = event.key === "ArrowLeft"
    ? Math.max(0, currentIndex - 1)
    : Math.min(slides.length - 1, currentIndex + 1);

  if (nextIndex !== currentIndex) {
    event.preventDefault();
    setPracticeCarouselIndex(carousel, nextIndex);
  }
});

