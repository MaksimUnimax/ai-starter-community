function isValidSectionId(sectionId) {
  return courseData.sections.some((section) => section.id === sectionId);
}

function getLessonIdFromUrl() {
  return new URLSearchParams(window.location.search).get("lesson");
}

function syncLessonQueryParam(sectionId) {
  if (!window.history || typeof window.history.replaceState !== "function") {
    return;
  }

  const url = new URL(window.location.href);
  url.searchParams.set("lesson", sectionId);
  window.history.replaceState({}, "", url.toString());
}

function escapeHTML(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll("\"", "&quot;");
}

function renderNavigation() {
  navRoot.innerHTML = courseData.sections
    .map((section) => {
      const activeClass = section.id === state.activeSectionId ? "is-active" : "";
      const navTitle = section.navTitle || section.title;
      return `
        <button class="nav-button ${activeClass}" type="button" data-section="${section.id}">
          <span class="nav-number">${escapeHTML(section.navLabel || section.eyebrow || "")}</span>
          <span class="nav-title">${escapeHTML(navTitle)}</span>
        </button>
      `;
    })
    .join("");
}

function flashcardKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function quizKey(sectionId, index) {
  return `${sectionId}:${index}`;
}

function renderLessonBlocks(section) {
  const blocks = Array.isArray(section.blocks) ? section.blocks : [];
  if (blocks.length === 0) {
    return section.contentHtml || "";
  }

  return blocks
    .map((block) => {
      const paragraphs = Array.isArray(block.paragraphs)
        ? block.paragraphs.map((paragraph) => `<p>${escapeHTML(paragraph)}</p>`).join("")
        : "";
      const items = Array.isArray(block.items) && block.items.length > 0
        ? `<${block.ordered ? "ol" : "ul"}>${block.items.map((item) => `<li>${escapeHTML(item)}</li>`).join("")}</${block.ordered ? "ol" : "ul"}>`
        : "";
      return `
        <section class="callout">
          <span class="block-label">${escapeHTML(block.title)}</span>
          ${paragraphs}
          ${items}
        </section>
      `;
    })
    .join("");
}

function getSectionQuizList(section) {
  if (Array.isArray(section.quizzes)) {
    return section.quizzes;
  }

  if (section.reviewQuiz) {
    return [section.quiz, ...section.reviewQuiz].filter(Boolean);
  }

  return [section.quiz].filter(Boolean);
}

function getSectionQuestionCount(section) {
  return getSectionQuizList(section).length;
}

function getSectionQuiz(section, index) {
  return getSectionQuizList(section)[index];
}

function getSectionAnsweredCount(section) {
  const questionCount = getSectionQuestionCount(section);
  let answeredCount = 0;
  for (let index = 0; index < questionCount; index += 1) {
    if (state.answeredQuestions[quizKey(section.id, index)] !== undefined) {
      answeredCount += 1;
    }
  }
  return answeredCount;
}

function renderFlashcards(section) {
  if (!section.flashcards || section.flashcards.length === 0) {
    return "";
  }

  return `
    <div class="module-grid">
      <div class="section-kicker">Карточки</div>
      <div class="flashcard-grid">
        ${section.flashcards
          .map((card, index) => {
            const key = flashcardKey(section.id, index);
            const revealed = state.flippedCards.has(key) ? "is-revealed" : "";
            return `
              <button class="flashcard ${revealed}" type="button" data-flashcard="${key}" aria-label="Перевернуть карточку">
                <strong class="flashcard-title">${escapeHTML(revealed ? card.back : card.front)}</strong>
                <span class="flashcard-note">${revealed ? "Нажмите ещё раз, чтобы вернуться." : "Нажми, чтобы увидеть ответ"}</span>
              </button>
            `;
          })
          .join("")}
      </div>
    </div>
  `;
}

function renderQuiz(section, quizList = getSectionQuizList(section), label = section.quizLabel || "Проверка знаний") {
  if (quizList.length === 0) {
    return "";
  }

  return `
    <div class="module-grid">
      <div class="section-kicker">${escapeHTML(label || "Проверка знаний")}</div>
      <div class="quiz-list">
        ${quizList
          .map((quiz, quizIndex) => {
            const questionKey = quizKey(section.id, quizIndex);
            const selectedIndex =
              state.answeredQuestions[questionKey] === undefined
                ? null
                : state.answeredQuestions[questionKey];
            const answered = selectedIndex !== null;
            const selectedCorrect = answered && selectedIndex === quiz.answerIndex;
            return `
              <div class="quiz-card" data-quiz-card="${questionKey}">
                <strong>${escapeHTML(quiz.prompt)}</strong>
                <div class="quiz-options">
                  ${quiz.options
                    .map((option, optionIndex) => {
                      const isCorrect = answered && optionIndex === quiz.answerIndex;
                      const isWrong = answered && selectedIndex === optionIndex && selectedIndex !== quiz.answerIndex;
                      const classes = ["quiz-option"];
                      if (isCorrect) classes.push("is-correct");
                      if (isWrong) classes.push("is-wrong");
                      return `
                        <button
                          class="${classes.join(" ")}"
                          type="button"
                          data-quiz="${questionKey}"
                          data-option="${optionIndex}"
                          ${answered ? "disabled" : ""}
                        >
                          ${escapeHTML(option)}
                        </button>
                      `;
                    })
                    .join("")}
                </div>
                <div class="feedback" id="feedback-${questionKey}">
                  ${
                    answered
                      ? selectedCorrect
                        ? `<strong>Верно.</strong> ${escapeHTML(quiz.explanation)}`
                        : `<strong>Почти.</strong> ${escapeHTML(quiz.explanation)}`
                      : "Выбери ответ, чтобы получить мгновенную обратную связь."
                  }
                </div>
              </div>
            `;
          })
          .join("")}
      </div>
    </div>
  `;
}

function renderIntro(section) {
  if (!section.intro || section.intro.length === 0) {
    return "";
  }

  return `
    <section class="callout">
      <span class="block-label">${escapeHTML(section.introLabel || "Вступление")}</span>
      ${section.intro.map((paragraph) => `<p>${escapeHTML(paragraph)}</p>`).join("")}
    </section>
  `;
}

function renderOutcome(section) {
  const outcome = section.learningOutcome || [];
  if (outcome.length === 0) {
    return "";
  }

  const body = outcome.length === 1
    ? `<p>${escapeHTML(outcome[0])}</p>`
    : `<ul>${outcome.map((item) => `<li>${escapeHTML(item)}</li>`).join("")}</ul>`;

  return `
    <section class="callout">
      <span class="block-label">${escapeHTML(section.learningOutcomeLabel || "После урока вы сможете")}</span>
      ${body}
    </section>
  `;
}

function renderStructuredLesson(section) {
  const intro = renderIntro(section);
  const outcome = renderOutcome(section);
  const quizList = getSectionQuizList(section);
  const quiz = renderQuiz(section, quizList, section.quizLabel || "Проверка знаний");
  const resultTitle = section.finalResultTitle || section.resultTitle || "Главный вывод урока";
  const resultText = section.finalResultText || section.resultText;
  const nextTitle = section.nextStepTitle || "Следующий урок";
  const nextText = section.nextStepText;
  const nextButton = section.nextStepButtonLabel;
  const finalResult = section.finalResultText
    ? `
        <section class="callout">
          <span class="block-label">${escapeHTML(resultTitle)}</span>
          <p>${escapeHTML(resultText)}</p>
        </section>
      `
    : "";
  const lessonResult = section.resultText && !section.finalResultText
    ? `
        <section class="callout">
          <span class="block-label">${escapeHTML(resultTitle)}</span>
          <p>${escapeHTML(resultText)}</p>
        </section>
      `
    : "";
  const nextStep = nextText
    ? `
        <section class="next-step-card">
          <div>
            <span class="block-label">${escapeHTML(nextTitle)}</span>
            <p>${escapeHTML(nextText)}</p>
          </div>
          ${nextButton ? `<button class="primary-button" type="button" data-section="${escapeHTML(section.nextStepTargetId || "lesson-2")}">${escapeHTML(nextButton)}</button>` : ""}
        </section>
      `
    : "";

  return `
    <article class="section-card">
      <header class="section-header">
        <p class="eyebrow">${escapeHTML(section.eyebrow)}</p>
        <h3>${escapeHTML(section.title)}</h3>
        <p class="muted">${escapeHTML(section.description)}</p>
      </header>

      <div class="section-body">
        ${intro}
        ${outcome}
        ${renderLessonBlocks(section)}
        ${quiz}
        ${lessonResult}
        ${finalResult}
        ${nextStep}
      </div>
    </article>
  `;
}

function renderSectionContent(section) {
  return renderStructuredLesson(section);
}

function updateProgress() {
  const section = courseData.sections.find((item) => item.id === state.activeSectionId) || courseData.sections[0];
  const totalQuestions = getSectionQuestionCount(section);
  const answeredQuestions = getSectionAnsweredCount(section);
  const progress = totalQuestions === 0 ? 0 : Math.min(100, Math.round((answeredQuestions / totalQuestions) * 100));

  progressFill.style.width = `${progress}%`;
  progressValue.textContent = `${progress}%`;

  if (progress === 0) {
    progressLabel.textContent = "Пока ничего не проверено.";
  } else if (answeredQuestions === 1) {
    progressLabel.textContent = `Выполнена 1 из ${totalQuestions} проверок.`;
  } else if (progress < 100) {
    progressLabel.textContent = `Выполнено ${answeredQuestions} из ${totalQuestions} проверок.`;
  } else {
    progressLabel.textContent = "Все проверки урока выполнены.";
  }
}

function renderActiveSection() {
  const section = courseData.sections.find((item) => item.id === state.activeSectionId) || courseData.sections[0];
  activeSectionRoot.innerHTML = renderSectionContent(section);
  renderNavigation();
  updateProgress();
}

function setActiveSection(sectionId, options = {}) {
  const normalizedSectionId = legacySectionAliases[sectionId] || sectionId;
  const resolvedSectionId = isValidSectionId(normalizedSectionId) ? normalizedSectionId : "lesson-1";
  const shouldUpdateUrl = options.updateUrl !== false;

  state.activeSectionId = resolvedSectionId;
  state.visitedSections.add(resolvedSectionId);
  if (shouldUpdateUrl) {
    syncLessonQueryParam(resolvedSectionId);
  }
  renderActiveSection();
}

document.addEventListener("click", (event) => {
  const navButton = event.target.closest("[data-section]");
  if (navButton) {
    setActiveSection(navButton.dataset.section);
    return;
  }

  const flashcard = event.target.closest("[data-flashcard]");
  if (flashcard) {
    state.flippedCards.has(flashcard.dataset.flashcard)
      ? state.flippedCards.delete(flashcard.dataset.flashcard)
      : state.flippedCards.add(flashcard.dataset.flashcard);
    renderActiveSection();
    return;
  }

  const quizButton = event.target.closest("[data-quiz]");
  if (quizButton) {
    const questionKey = quizButton.dataset.quiz;
    const [sectionId, quizIndexText] = questionKey.split(":");
    const quizIndex = Number(quizIndexText);
    const section = courseData.sections.find((item) => item.id === sectionId);
    const quiz = section ? getSectionQuiz(section, quizIndex) : null;
    if (!quiz) {
      return;
    }
    const selectedIndex = Number(quizButton.dataset.option);
    state.answeredQuestions[questionKey] = selectedIndex;
    if (selectedIndex === quiz.answerIndex) {
      state.solvedQuestions.add(questionKey);
    }
    renderActiveSection();
    return;
  }

  const startButton = event.target.closest("#start-learning");
  if (startButton) {
    setActiveSection("lesson-1");
    return;
  }

  const reviewButton = event.target.closest("#open-review");
  if (reviewButton) {
    setActiveSection("review");
    return;
  }
});

